package liuyifan.demo.com.test_3.Entities;

public class LivingLevel {
    private String city;
    private String car_washing;
    private String comfort;
    private String dressing;
    private String road_condition;
    private String sport;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCar_washing() {
        return car_washing;
    }

    public void setCar_washing(String car_washing) {
        this.car_washing = car_washing;
    }

    public String getComfort() {
        return comfort;
    }

    public void setComfort(String comfort) {
        this.comfort = comfort;
    }

    public String getDressing() {
        return dressing;
    }

    public void setDressing(String dressing) {
        this.dressing = dressing;
    }

    public String getRoad_condition() {
        return road_condition;
    }

    public void setRoad_condition(String road_condition) {
        this.road_condition = road_condition;
    }

    public String getSport() {
        return sport;
    }

    public void setSport(String sport) {
        this.sport = sport;
    }
}
